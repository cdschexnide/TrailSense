/**
 * Icon usage guidelines
 *
 * 1. Use outline variants for unselected/default states
 * 2. Use filled variants for selected/active states
 * 3. Match icon weight to text weight when possible
 * 4. Ensure adequate touch target (44x44pt minimum)
 * 5. Use consistent sizing within a component
 * 6. Color icons using theme colors (systemBlue for interactive, etc.)
 */
